import Foundation

class SpringData {
    var springnames: String
    var springlocations: String
    var springprices: String
    var springstars: String
    var springmains: String
    
    init(springname: String, springlocation: String,  springprice: String,  springstar: String, springmain: String) {
        springnames = springname
        springlocations = springlocation
        springprices = springprice
        springstars = springstar
        springmains = springmain
    }
}
